//go:build slow

package cpp

import (
	"bytes"
	"fmt"
	"strings"

	"mochi/parser"
)

// Compiler is a very small Mochi to C++ translator used in tests. It
// intentionally supports only a subset of the language. Unsupported
// constructs return an error so that tests can record failures.
type Compiler struct {
	buf    bytes.Buffer
	indent int
}

// New returns a new compiler instance.
func New() *Compiler { return &Compiler{} }

func (c *Compiler) writeln(s string) {
	for i := 0; i < c.indent; i++ {
		c.buf.WriteByte('\t')
	}
	c.buf.WriteString(s)
	c.buf.WriteByte('\n')
}

func (c *Compiler) writeIndent() {
	for i := 0; i < c.indent; i++ {
		c.buf.WriteByte('\t')
	}
}

// Compile translates the given program into C++ source code.
func (c *Compiler) Compile(p *parser.Program) ([]byte, error) {
	c.buf.Reset()
	c.writeln("// Generated by Mochi experimental C++ compiler")
	c.writeln("#include <bits/stdc++.h>")
	c.writeln("using namespace std;")
	c.writeln("")

	// compile function declarations first
	for _, st := range p.Statements {
		if st.Fun != nil {
			if err := c.compileFun(st.Fun); err != nil {
				return nil, err
			}
			c.writeln("")
		}
	}

	// main body
	c.writeln("int main() {")
	c.indent++
	for _, st := range p.Statements {
		if st.Fun != nil || st.Type != nil {
			continue
		}
		if err := c.compileStmt(st); err != nil {
			return nil, err
		}
	}
	c.writeln("return 0;")
	c.indent--
	c.writeln("}")

	return c.buf.Bytes(), nil
}

func (c *Compiler) compileFun(fn *parser.FunStmt) error {
	c.writeIndent()
	c.buf.WriteString("auto ")
	c.buf.WriteString(fn.Name)
	c.buf.WriteString("(")
	for i, p := range fn.Params {
		if i > 0 {
			c.buf.WriteString(", ")
		}
		c.buf.WriteString("auto ")
		c.buf.WriteString(p.Name)
	}
	c.buf.WriteString(") {\n")
	c.indent++
	for _, st := range fn.Body {
		if err := c.compileStmt(st); err != nil {
			return err
		}
	}
	c.indent--
	c.writeln("}")
	return nil
}

func (c *Compiler) compileStmt(st *parser.Statement) error {
	switch {
	case st.Let != nil:
		c.writeIndent()
		c.buf.WriteString("auto ")
		c.buf.WriteString(st.Let.Name)
		if st.Let.Value != nil {
			val, err := c.compileExpr(st.Let.Value)
			if err != nil {
				return err
			}
			c.buf.WriteString(" = ")
			c.buf.WriteString(val)
		}
		c.buf.WriteString(";\n")
	case st.Var != nil:
		c.writeIndent()
		c.buf.WriteString("auto ")
		c.buf.WriteString(st.Var.Name)
		if st.Var.Value != nil {
			val, err := c.compileExpr(st.Var.Value)
			if err != nil {
				return err
			}
			c.buf.WriteString(" = ")
			c.buf.WriteString(val)
		}
		c.buf.WriteString(";\n")
	case st.Assign != nil:
		c.writeIndent()
		rhs, err := c.compileExpr(st.Assign.Value)
		if err != nil {
			return err
		}
		c.buf.WriteString(st.Assign.Name)
		c.buf.WriteString(" = ")
		c.buf.WriteString(rhs)
		c.buf.WriteString(";\n")
	case st.Return != nil:
		c.writeIndent()
		val, err := c.compileExpr(st.Return.Value)
		if err != nil {
			return err
		}
		c.buf.WriteString("return ")
		c.buf.WriteString(val)
		c.buf.WriteString(";\n")
	case st.Expr != nil:
		c.writeIndent()
		val, err := c.compileExpr(st.Expr.Expr)
		if err != nil {
			return err
		}
		c.buf.WriteString(val)
		c.buf.WriteString(";\n")
	case st.If != nil:
		return c.compileIf(st.If)
	case st.While != nil:
		cond, err := c.compileExpr(st.While.Cond)
		if err != nil {
			return err
		}
		c.writeIndent()
		c.buf.WriteString("while(" + cond + ") {\n")
		c.indent++
		for _, b := range st.While.Body {
			if err := c.compileStmt(b); err != nil {
				return err
			}
		}
		c.indent--
		c.writeln("}")
	case st.For != nil:
		return c.compileFor(st.For)
	case st.Break != nil:
		c.writeIndent()
		c.writeln("break;")
	case st.Continue != nil:
		c.writeIndent()
		c.writeln("continue;")
	default:
		return fmt.Errorf("unsupported statement at %v", st.Pos)
	}
	return nil
}

func (c *Compiler) compileIf(st *parser.IfStmt) error {
	cond, err := c.compileExpr(st.Cond)
	if err != nil {
		return err
	}
	c.writeIndent()
	c.buf.WriteString("if(" + cond + ") {\n")
	c.indent++
	for _, b := range st.Then {
		if err := c.compileStmt(b); err != nil {
			return err
		}
	}
	c.indent--
	c.writeIndent()
	c.buf.WriteString("}")
	if st.Else != nil {
		c.buf.WriteString(" else {\n")
		c.indent++
		for _, b := range st.Else {
			if err := c.compileStmt(b); err != nil {
				return err
			}
		}
		c.indent--
		c.writeln("}")
	} else {
		c.writeln("")
	}
	return nil
}

func (c *Compiler) compileFor(st *parser.ForStmt) error {
	if st.RangeEnd != nil {
		start, err := c.compileExpr(st.Source)
		if err != nil {
			return err
		}
		end, err := c.compileExpr(st.RangeEnd)
		if err != nil {
			return err
		}
		iter := st.Name
		c.writeIndent()
		c.buf.WriteString(fmt.Sprintf("for(int %s=%s; %s<%s; ++%s) {\n", iter, start, iter, end, iter))
		c.indent++
		for _, b := range st.Body {
			if err := c.compileStmt(b); err != nil {
				return err
			}
		}
		c.indent--
		c.writeln("}")
		return nil
	}
	src, err := c.compileExpr(st.Source)
	if err != nil {
		return err
	}
	iter := st.Name
	c.writeIndent()
	c.buf.WriteString(fmt.Sprintf("for(auto %s : %s) {\n", iter, src))
	c.indent++
	for _, b := range st.Body {
		if err := c.compileStmt(b); err != nil {
			return err
		}
	}
	c.indent--
	c.writeln("}")
	return nil
}

func (c *Compiler) compileExpr(e *parser.Expr) (string, error) {
	if e == nil {
		return "", nil
	}
	if e.Binary != nil {
		left, err := c.compileUnary(e.Binary.Left)
		if err != nil {
			return "", err
		}
		result := left
		for _, op := range e.Binary.Right {
			rhs, err := c.compilePostfix(op.Right)
			if err != nil {
				return "", err
			}
			result = fmt.Sprintf("(%s %s %s)", result, op.Op, rhs)
		}
		return result, nil
	}
	return "", fmt.Errorf("unsupported expression at %v", e.Pos)
}

func (c *Compiler) compileUnary(u *parser.Unary) (string, error) {
	val, err := c.compilePostfix(u.Value)
	if err != nil {
		return "", err
	}
	for i := len(u.Ops) - 1; i >= 0; i-- {
		val = fmt.Sprintf("(%s%s)", u.Ops[i], val)
	}
	return val, nil
}

func (c *Compiler) compilePostfix(pf *parser.PostfixExpr) (string, error) {
	target, err := c.compilePrimary(pf.Target)
	if err != nil {
		return "", err
	}
	expr := target
	for _, op := range pf.Ops {
		switch {
		case op.Call != nil:
			args := []string{}
			for _, a := range op.Call.Args {
				s, err := c.compileExpr(a)
				if err != nil {
					return "", err
				}
				args = append(args, s)
			}
			if expr == "print" {
				expr = fmt.Sprintf("([&](){ cout << %s << endl; })()", strings.Join(args, " << \" \" << "))
			} else {
				expr = fmt.Sprintf("%s(%s)", expr, strings.Join(args, ", "))
			}
		case op.Index != nil:
			idx, err := c.compileExpr(op.Index.Start)
			if err != nil {
				return "", err
			}
			expr = fmt.Sprintf("%s[%s]", expr, idx)
		case op.Field != nil:
			expr = fmt.Sprintf("%s.%s", expr, op.Field.Name)
		default:
			return "", fmt.Errorf("unsupported postfix op")
		}
	}
	return expr, nil
}

func (c *Compiler) compilePrimary(p *parser.Primary) (string, error) {
	switch {
	case p.Selector != nil:
		name := p.Selector.Root
		for _, t := range p.Selector.Tail {
			name += "." + t
		}
		return name, nil
	case p.Call != nil:
		args := []string{}
		for _, e := range p.Call.Args {
			s, err := c.compileExpr(e)
			if err != nil {
				return "", err
			}
			args = append(args, s)
		}
		fn := p.Call.Func
		if fn == "print" {
			return fmt.Sprintf("([&](){ cout << %s << endl; })()", strings.Join(args, " << \" \" << ")), nil
		}
		return fn + "(" + strings.Join(args, ", ") + ")", nil
	case p.Lit != nil:
		return c.compileLiteral(p.Lit)
	case p.List != nil:
		elems := []string{}
		for _, e := range p.List.Elems {
			s, err := c.compileExpr(e)
			if err != nil {
				return "", err
			}
			elems = append(elems, s)
		}
		return "vector<int>{" + strings.Join(elems, ", ") + "}", nil
	case p.Group != nil:
		return c.compileExpr(p.Group)
	default:
		return "", fmt.Errorf("unsupported primary at %v", p.Pos)
	}
}

func (c *Compiler) compileLiteral(l *parser.Literal) (string, error) {
	if l.Int != nil {
		return fmt.Sprint(*l.Int), nil
	}
	if l.Float != nil {
		return fmt.Sprint(*l.Float), nil
	}
	if l.Bool != nil {
		if bool(*l.Bool) {
			return "true", nil
		}
		return "false", nil
	}
	if l.Str != nil {
		return fmt.Sprintf("\"%s\"", *l.Str), nil
	}
	return "", fmt.Errorf("unknown literal")
}
