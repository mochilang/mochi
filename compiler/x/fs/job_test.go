//go:build slow

package fscode_test

import (
	"bytes"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"testing"

	fscode "mochi/compiler/x/fs"
	"mochi/parser"
	"mochi/types"
)

func stripHeaderJob(b []byte) []byte {
	if i := bytes.IndexByte(b, '\n'); i != -1 && bytes.HasPrefix(b, []byte("// Generated by Mochi")) {
		return b[i+1:]
	}
	return b
}

func TestFSCompiler_JOB(t *testing.T) {
	if _, err := exec.LookPath("fsharpc"); err != nil {
		t.Skip("fsharpc not installed")
	}
	if _, err := exec.LookPath("mono"); err != nil {
		t.Skip("mono not installed")
	}
	root := repoRoot(t)

	runQuery := func(q string) {
		src := filepath.Join(root, "tests", "dataset", "job", q+".mochi")
		codeWant := filepath.Join(root, "tests", "dataset", "job", "compiler", "fs", q+".fs.out")
		outWant := filepath.Join(root, "tests", "dataset", "job", "out", q+".out")

		prog, err := parser.Parse(src)
		if err != nil {
			t.Fatalf("parse error: %v", err)
		}
		env := types.NewEnv(nil)
		if errs := types.Check(prog, env); len(errs) > 0 {
			t.Fatalf("type error: %v", errs[0])
		}
		code, err := fscode.CompileFile(src)
		if err != nil {
			t.Fatalf("compile error: %v", err)
		}
		wantCode, err := os.ReadFile(codeWant)
		if err != nil {
			t.Fatalf("read golden: %v", err)
		}
		got := stripHeaderJob(bytes.TrimSpace(code))
		want := bytes.TrimSpace(wantCode)
		if !bytes.Equal(got, want) {
			t.Errorf("generated code mismatch for %s.fs.out\n\n--- Got ---\n%s\n\n--- Want ---\n%s\n", q, got, want)
		}
		dir := t.TempDir()
		fsPath := filepath.Join(dir, "main.fs")
		if err := os.WriteFile(fsPath, code, 0644); err != nil {
			t.Fatalf("write error: %v", err)
		}
		exe := filepath.Join(dir, "main.exe")
		jsonRef := "/usr/lib/dotnet/shared/Microsoft.NETCore.App/8.0.17/System.Text.Json.dll"
		runtimeRef := "/usr/lib/dotnet/shared/Microsoft.NETCore.App/8.0.17/System.Runtime.dll"
		cmd := exec.Command("fsharpc", "--target:exe", fmt.Sprintf("--out:%s", exe), "-r:"+jsonRef, "-r:"+runtimeRef, fsPath)
		out, err := cmd.CombinedOutput()
		if err != nil {
			t.Fatalf("fsharpc error: %v\n%s", err, out)
		}
		run := exec.Command("mono", exe)
		var stdout bytes.Buffer
		run.Stdout = &stdout
		run.Stderr = &stdout
		if err := run.Run(); err != nil {
			t.Fatalf("mono error: %v\n%s", err, stdout.Bytes())
		}
		gotOut := bytes.TrimSpace(stdout.Bytes())
		wantOut, err := os.ReadFile(outWant)
		if err != nil {
			t.Fatalf("read golden: %v", err)
		}
		if !bytes.Equal(gotOut, bytes.TrimSpace(wantOut)) {
			t.Errorf("output mismatch for %s.out\n\n--- Got ---\n%s\n\n--- Want ---\n%s\n", q, gotOut, bytes.TrimSpace(wantOut))
		}
	}

	for _, q := range []string{"q1", "q2", "q3", "q4", "q5"} {
		t.Run(q, func(t *testing.T) { runQuery(q) })
	}
}
