//go:build slow

package st_test

import (
	"bytes"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strings"
	"testing"

	st "mochi/compiler/x/st"
	"mochi/compiler/x/testutil"
	"mochi/parser"
	"mochi/types"
)

func shouldUpdateRosetta() bool {
	if v, ok := os.LookupEnv("UPDATE"); ok && (v == "1" || v == "true") {
		return true
	}
	return false
}

func stripHeaderRosetta(b []byte) []byte {
	if i := bytes.IndexByte(b, '\n'); i != -1 && bytes.HasPrefix(b, []byte("\" Generated by Mochi")) {
		return b[i+1:]
	}
	return b
}

func runRosettaTaskGolden(t *testing.T, name string) {
	gst := ensureGST()
	root := testutil.FindRepoRoot(t)
	outDir := filepath.Join(root, "tests", "rosetta", "out", "Smalltalk")
	src := filepath.Join(root, "tests", "rosetta", "x", "Mochi", name+".mochi")
	prog, err := parser.Parse(src)
	if err != nil {
		t.Fatalf("parse error: %v", err)
	}
	env := types.NewEnv(nil)
	if errs := types.Check(prog, env); len(errs) > 0 {
		t.Fatalf("type error: %v", errs[0])
	}
	code, err := st.New().Compile(prog)
	if err != nil {
		t.Fatalf("compile error: %v", err)
	}
	codeWant := filepath.Join(outDir, name+".st")
	if shouldUpdateRosetta() {
		_ = os.WriteFile(codeWant, code, 0644)
	} else if want, err := os.ReadFile(codeWant); err == nil {
		got := stripHeaderRosetta(bytes.TrimSpace(code))
		want = stripHeaderRosetta(bytes.TrimSpace(want))
		if !bytes.Equal(got, want) {
			t.Errorf("generated code mismatch for %s.st\n\n--- Got ---\n%s\n\n--- Want ---\n%s", name, got, want)
		}
	}
	dir := t.TempDir()
	file := filepath.Join(dir, name+".st")
	if err := os.WriteFile(file, code, 0644); err != nil {
		t.Fatalf("write error: %v", err)
	}
	if gst == "" {
		_ = os.WriteFile(filepath.Join(outDir, name+".error"), []byte("gst interpreter not available"), 0644)
		return
	}
	cmd := exec.Command(gst, file)
	var buf bytes.Buffer
	cmd.Stderr = &buf
	out, err := cmd.Output()
	if err != nil {
		_ = os.WriteFile(filepath.Join(outDir, name+".error"), []byte(fmt.Sprintf("run: %v\n%s", err, buf.String())), 0644)
		return
	}
	_ = os.Remove(filepath.Join(outDir, name+".error"))
	outPath := filepath.Join(outDir, name+".out")
	gotOut := bytes.TrimSpace(out)
	if shouldUpdateRosetta() {
		_ = os.WriteFile(outPath, append(gotOut, '\n'), 0644)
	} else if wantOut, err := os.ReadFile(outPath); err == nil {
		if !bytes.Equal(gotOut, bytes.TrimSpace(wantOut)) {
			t.Errorf("output mismatch for %s.out\n\n--- Got ---\n%s\n\n--- Want ---\n%s", name, gotOut, bytes.TrimSpace(wantOut))
		}
	}
}

func TestSmalltalkCompiler_Rosetta_Golden(t *testing.T) {
	if ensureGST() == "" {
		t.Skip("gst not installed")
	}
	root := testutil.FindRepoRoot(t)
	files, err := filepath.Glob(filepath.Join(root, "tests", "rosetta", "x", "Mochi", "*.mochi"))
	if err != nil {
		t.Fatalf("glob: %v", err)
	}
	max := 3
	if len(files) < max {
		max = len(files)
	}
	for _, f := range files[:max] {
		name := strings.TrimSuffix(filepath.Base(f), ".mochi")
		t.Run(name, func(t *testing.T) { runRosettaTaskGolden(t, name) })
	}
}
