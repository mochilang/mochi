var updateTPCDS = flag.Bool("update-tpcds", false, "update TPC-DS golden files")

func shouldUpdateTPCDS() bool {
	f := flag.Lookup("update-tpcds")
	return f != nil && f.Value.String() == "true"
}

func repoRootTPCDS(t *testing.T) string {
	dir, err := os.Getwd()
	if err != nil {
		t.Fatal("cannot determine working directory")
	}
	for i := 0; i < 10; i++ {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}
	t.Fatal("go.mod not found")
	return ""
}

func stripHeaderTPCDS(b []byte) []byte {
	if idx := bytes.Index(b, []byte("// Generated by Mochi")); idx != -1 {
		end := bytes.IndexByte(b[idx:], '\n')
		if end != -1 {
			return append(b[:idx], b[idx+end+1:]...)
		}
		return b[:idx]
	}
	return b
}

func normalizeOutputTPCDS(root string, b []byte) []byte {
	out := string(b)
	out = strings.ReplaceAll(out, filepath.ToSlash(root)+"/", "")
	out = strings.ReplaceAll(out, filepath.ToSlash(root), "")
	out = strings.ReplaceAll(out, "github.com/mochi-lang/mochi/", "")
	out = strings.ReplaceAll(out, "mochi/tests/", "tests/")
	durRE := regexp.MustCompile(`\([0-9]+(\.[0-9]+)?(ns|Âµs|ms|s)\)`)
	out = durRE.ReplaceAllString(out, "(X)")
	out = strings.TrimSpace(out)
	return []byte(out)
}

func writeErrTPCDS(root, base string, err error) {
	_ = os.WriteFile(filepath.Join(root, "tests", "dataset", "tpc-ds", "compiler", "go", base+".error"), []byte(err.Error()), 0644)
}

func removeErrTPCDS(root, base string) {
	_ = os.Remove(filepath.Join(root, "tests", "dataset", "tpc-ds", "compiler", "go", base+".error"))
}

func runTPCDSQuery(t *testing.T, base string) {
	root := repoRootTPCDS(t)
	src := filepath.Join(root, "tests", "dataset", "tpc-ds", base+".mochi")

	prog, err := parser.Parse(src)
	if err != nil {
		t.Fatalf("parse error: %v", err)
	}
	env := types.NewEnv(nil)
	if errs := types.Check(prog, env); len(errs) > 0 {
		t.Fatalf("type error: %v", errs[0])
	}

	code, err := gocode.New(env).Compile(prog)
	if err != nil {
		writeErrTPCDS(root, base, fmt.Errorf("compile error: %w", err))
		t.Skipf("compile error: %v", err)
		return
	}

	codePath := filepath.Join(root, "tests", "dataset", "tpc-ds", "compiler", "go", base+".go")
	_ = os.WriteFile(codePath, code, 0o644)

	dir := t.TempDir()
	file := filepath.Join(dir, "main.go")
	if err := os.WriteFile(file, code, 0644); err != nil {
		t.Fatalf("write error: %v", err)
	}
	cmd := exec.Command("go", "run", file)
	out, err := cmd.CombinedOutput()
	if err != nil {
		writeErrTPCDS(root, base, fmt.Errorf("go run error: %w\n%s", err, out))
		t.Skipf("go run error: %v\n%s", err, out)
		return
	}
	gotOut := bytes.TrimSpace(out)
	if idx := bytes.IndexByte(gotOut, '\n'); idx >= 0 {
		gotOut = gotOut[:idx]
	}

	p, err := vm.Compile(prog, env)
	if err != nil {
		writeErrTPCDS(root, base, fmt.Errorf("vm compile error: %w", err))
		t.Skipf("vm compile error: %v", err)
		return
	}
	var vmBuf bytes.Buffer
	m := vm.New(p, &vmBuf)
	if err := m.Run(); err != nil {
		writeErrTPCDS(root, base, fmt.Errorf("vm run error: %w", err))
		t.Skipf("vm run error: %v", err)
		return
	}
	vmOut := bytes.TrimSpace(vmBuf.Bytes())
	if idx := bytes.IndexByte(vmOut, '\n'); idx >= 0 {
		vmOut = vmOut[:idx]
	}
	if !bytes.Equal(normalizeOutputTPCDS(root, gotOut), normalizeOutputTPCDS(root, vmOut)) {
		writeErrTPCDS(root, base, fmt.Errorf("vm mismatch\n-- go --\n%s\n-- vm --\n%s", gotOut, vmOut))
		t.Skipf("vm mismatch for %s", base)
		return
	}

	outWant := filepath.Join(root, "tests", "dataset", "tpc-ds", "out", base+".out")
	if shouldUpdateTPCDS() {
		_ = os.WriteFile(filepath.Join(root, "tests", "dataset", "tpc-ds", "compiler", "go", base+".out"), append(gotOut, '\n'), 0o644)
	}
	wantOut, err := os.ReadFile(outWant)
	if err != nil {
		t.Skipf("read golden: %v", err)
		return
	}
	wantOut = bytes.TrimSpace(wantOut)
	if idx := bytes.IndexByte(wantOut, '\n'); idx >= 0 {
		wantOut = wantOut[:idx]
	}
	if !bytes.Equal(normalizeOutputTPCDS(root, gotOut), normalizeOutputTPCDS(root, wantOut)) {
		writeErrTPCDS(root, base, fmt.Errorf("output mismatch\n-- got --\n%s\n-- want --\n%s", gotOut, wantOut))
		t.Skipf("output mismatch for %s", base)
		return
	}
	removeErrTPCDS(root, base)
}

func TestGoCompiler_TPCDS(t *testing.T) {
	if _, err := exec.LookPath("go"); err != nil {
		t.Skip("go toolchain not installed")
	}
	for i := 1; i <= 99; i++ {
		base := fmt.Sprintf("q%d", i)
		if _, err := os.Stat(filepath.Join(repoRootTPCDS(t), "tests", "dataset", "tpc-ds", base+".mochi")); err != nil {
			continue
		}
		t.Run(base, func(t *testing.T) { runTPCDSQuery(t, base) })
	}
}
