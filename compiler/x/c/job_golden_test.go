//go:build slow

package ccode_test

import (
	"bytes"
	"flag"
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"testing"

	ccode "mochi/compiler/x/c"
	"mochi/parser"
	"mochi/runtime/vm"
	"mochi/types"
)

var jobUpdate = flag.Bool("update-job", false, "update golden files")

func shouldUpdate() bool {
	f := flag.Lookup("update-job")
	return f != nil && f.Value.String() == "true"
}

func repoRoot(t *testing.T) string {
	dir, err := os.Getwd()
	if err != nil {
		t.Fatal("cannot determine working directory")
	}
	for i := 0; i < 10; i++ {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}
	t.Fatal("go.mod not found")
	return ""
}

func stripHeader(b []byte) []byte {
	if i := bytes.IndexByte(b, '\n'); i != -1 && bytes.HasPrefix(b, []byte("// Generated by Mochi")) {
		return b[i+1:]
	}
	return b
}

func TestCCompiler_JOB_Golden(t *testing.T) {
	cc, err := ccode.EnsureCC()
	if err != nil {
		t.Skipf("C compiler not installed: %v", err)
	}
	root := repoRoot(t)
	for i := 1; i <= 33; i++ {
		query := fmt.Sprintf("q%d", i)
		t.Run(query, func(t *testing.T) {
			src := filepath.Join(root, "tests", "dataset", "job", query+".mochi")
			prog, err := parser.Parse(src)
			if err != nil {
				t.Fatalf("parse error: %v", err)
			}
			env := types.NewEnv(nil)
			if errs := types.Check(prog, env); len(errs) > 0 {
				t.Fatalf("type error: %v", errs[0])
			}
			os.Setenv("SOURCE_DATE_EPOCH", "1136214245")
			code, err := ccode.New(env).Compile(prog)
			errPath := filepath.Join(root, "tests", "dataset", "job", "compiler", "c", query+".error")
			if err != nil {
				if shouldUpdate() {
					os.WriteFile(errPath, []byte(err.Error()), 0644)
				}
				return
			}
			os.Remove(errPath)
			wantCodePath := filepath.Join(root, "tests", "dataset", "job", "compiler", "c", query+".c")
			gotCode := stripHeader(bytes.TrimSpace(code))
			if shouldUpdate() {
				if err := os.WriteFile(wantCodePath, append(gotCode, '\n'), 0644); err != nil {
					t.Fatalf("write golden: %v", err)
				}
			} else {
				wantCode, err := os.ReadFile(wantCodePath)
				if err != nil {
					t.Fatalf("read golden: %v", err)
				}
				wantCode = stripHeader(bytes.TrimSpace(wantCode))
				if !bytes.Equal(gotCode, wantCode) {
					t.Errorf("generated code mismatch for %s\n\n--- Got ---\n%s\n\n--- Want ---\n%s\n", query+".c", gotCode, wantCode)
				}
			}
			dir := t.TempDir()
			cfile := filepath.Join(dir, "prog.c")
			if err := os.WriteFile(cfile, code, 0644); err != nil {
				t.Fatalf("write error: %v", err)
			}
			bin := filepath.Join(dir, "prog")
			if out, err := exec.Command(cc, cfile, "-o", bin).CombinedOutput(); err != nil {
				if shouldUpdate() {
					os.WriteFile(errPath, out, 0644)
				}
				return
			}
			out, err := exec.Command(bin).CombinedOutput()
			if err != nil {
				if shouldUpdate() {
					os.WriteFile(errPath, out, 0644)
				}
				return
			}
			os.Remove(errPath)
			gotOut := bytes.TrimSpace(out)

			p, err := vm.Compile(prog, env)
			if err != nil {
				t.Errorf("vm compile error: %v", err)
			} else {
				var vmBuf bytes.Buffer
				m := vm.New(p, &vmBuf)
				if err := m.Run(); err != nil {
					t.Errorf("vm run error: %v", err)
				} else {
					vmOut := bytes.TrimSpace(vmBuf.Bytes())
					if !bytes.Equal(gotOut, vmOut) {
						t.Errorf("vm mismatch for %s\n\n--- VM ---\n%s\n\n--- C ---\n%s\n", query, vmOut, gotOut)
					}
				}
			}
			wantOutPath := filepath.Join(root, "tests", "dataset", "job", "out", query+".out")
			if shouldUpdate() {
				os.WriteFile(wantOutPath, append(gotOut, '\n'), 0644)
			} else {
				wantOut, err := os.ReadFile(wantOutPath)
				if err != nil {
					t.Fatalf("read golden: %v", err)
				}
				if !bytes.Equal(gotOut, bytes.TrimSpace(wantOut)) {
					t.Errorf("output mismatch for %s\n\n--- Got ---\n%s\n\n--- Want ---\n%s\n", query+".out", gotOut, bytes.TrimSpace(wantOut))
				}
			}
		})
	}
}
