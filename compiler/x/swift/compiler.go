package swift

import (
	"strings"
)

// Compile returns a basic Swift program that contains the original Mochi source
// code in comments and prints a stub message. It does not attempt to fully
// translate Mochi semantics.
func Compile(src string) ([]byte, error) {
	var b strings.Builder
	b.WriteString("// Generated by Mochi Swift compiler (stub)\n")
	b.WriteString("import Foundation\n\n")
	b.WriteString("/* Original Mochi code:\n")
	b.WriteString(src)
	if !strings.HasSuffix(src, "\n") {
		b.WriteString("\n")
	}
	b.WriteString("*/\n\n")
	b.WriteString("print(\"mochi swift compiler stub\")\n")
	return []byte(b.String()), nil
}
