func ensureFortran(t *testing.T) string {
	t.Helper()
	path, err := ftncode.EnsureFortran()
	if err != nil {
		t.Skipf("gfortran missing: %v", err)
	}
	return path
}

func TestCompilePrograms(t *testing.T) {
	gfortran := ensureFortran(t)
	root := testutil.FindRepoRoot(t)
	outDir := filepath.Join(root, "tests", "machine", "x", "fortran")
	os.MkdirAll(outDir, 0755)
	files, err := filepath.Glob(filepath.Join(root, "tests", "vm", "valid", "*.mochi"))
	if err != nil {
		t.Fatalf("glob: %v", err)
	}
	for _, src := range files {
		name := strings.TrimSuffix(filepath.Base(src), ".mochi")
		t.Run(name, func(t *testing.T) { compileOne(t, src, outDir, name, gfortran) })
	}
}

func compileOne(t *testing.T, src, outDir, name, gfortran string) {
	data, err := os.ReadFile(src)
	if err != nil {
		t.Fatalf("read: %v", err)
	}
       prog, err := parser.Parse(src)
       if err != nil {
		writeError(outDir, name, data, err)
		t.Skipf("parse error: %v", err)
		return
	}
       env := types.NewEnv(nil)
       os.Setenv("MOCHI_HEADER_TIME", "2006-01-02T15:04:05Z")
       defer os.Unsetenv("MOCHI_HEADER_TIME")
       if errs := types.Check(prog, env); len(errs) > 0 {
		writeError(outDir, name, data, errs[0])
		t.Skipf("type error: %v", errs[0])
		return
	}
	code, err := ftncode.New(env).Compile(prog)
	if err != nil {
		writeError(outDir, name, data, err)
		t.Skipf("compile error: %v", err)
		return
	}
	f90 := filepath.Join(outDir, name+".f90")
	if err := os.WriteFile(f90, code, 0644); err != nil {
		t.Fatalf("write f90: %v", err)
	}
	exe := filepath.Join(outDir, name)
	if out, err := exec.Command(gfortran, f90, "-static", "-o", exe).CombinedOutput(); err != nil {
		writeError(outDir, name, code, fmt.Errorf("gfortran: %v\n%s", err, out))
		t.Skipf("gfortran: %v", err)
		return
	}
	defer os.Remove(exe)
	cmd := exec.Command(exe)
	if in, err := os.ReadFile(strings.TrimSuffix(src, ".mochi") + ".in"); err == nil {
		cmd.Stdin = bytes.NewReader(in)
	}
	out, err := cmd.CombinedOutput()
	if err != nil {
		writeError(outDir, name, code, fmt.Errorf("run error: %v\n%s", err, out))
		t.Skipf("run error: %v", err)
		return
	}
	os.WriteFile(filepath.Join(outDir, name+".out"), bytes.TrimSpace(out), 0644)
	errPath := filepath.Join(outDir, name+".error")
	os.Remove(errPath)
}

func writeError(dir, name string, src []byte, err error) {
	var buf bytes.Buffer
	if d, ok := err.(diagnostic.Diagnostic); ok {
		buf.WriteString(d.Format())
	} else {
		line := 0
		re := regexp.MustCompile(`line (\d+)`)
		if m := re.FindStringSubmatch(err.Error()); len(m) == 2 {
			line, _ = strconv.Atoi(m[1])
		}
		fmt.Fprintf(&buf, "error: %v\n", err)
		if line > 0 {
			lines := strings.Split(string(src), "\n")
			start := line - 2
			if start < 0 {
				start = 0
			}
			end := line + 1
			if end > len(lines) {
				end = len(lines)
			}
			for i := start; i < end; i++ {
				fmt.Fprintf(&buf, "%4d| %s\n", i+1, lines[i])
			}
		}
	}
	os.WriteFile(filepath.Join(dir, name+".error"), buf.Bytes(), 0644)
}

func TestMain(m *testing.M) {
	code := m.Run()
	updateReadme()
	os.Exit(code)
}

func updateReadme() {
	root := testutil.FindRepoRoot(&testing.T{})
	srcDir := filepath.Join(root, "tests", "vm", "valid")
	outDir := filepath.Join(root, "tests", "machine", "x", "fortran")
	files, _ := filepath.Glob(filepath.Join(srcDir, "*.mochi"))
	total := len(files)
	compiled := 0
	var lines []string
	for _, f := range files {
		name := strings.TrimSuffix(filepath.Base(f), ".mochi")
		mark := "[ ]"
		if _, err := os.Stat(filepath.Join(outDir, name+".out")); err == nil {
			compiled++
			mark = "[x]"
		}
		lines = append(lines, fmt.Sprintf("- %s %s", mark, name))
	}
	var buf bytes.Buffer
	buf.WriteString("# Generated Fortran Programs\n\n")
	buf.WriteString("This directory contains Fortran source code and outputs generated by the compiler tests. Each Mochi program from `tests/vm/valid` is compiled to a Fortran `.f90` file. Successful runs have a corresponding `.out` file. If compilation or execution fails, an `.error` file is written.\n\n")
	fmt.Fprintf(&buf, "Compiled programs: %d/%d\n\n", compiled, total)
	buf.WriteString("Checklist:\n\n")
	buf.WriteString(strings.Join(lines, "\n"))
	buf.WriteString("\n")
	os.WriteFile(filepath.Join(outDir, "README.md"), buf.Bytes(), 0644)
}
