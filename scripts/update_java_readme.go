//go:build ignore

package main

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

func main() {
	root := "."
	srcDir := filepath.Join(root, "tests", "vm", "valid")
	outDir := filepath.Join(root, "tests", "machine", "x", "java")
	files, _ := filepath.Glob(filepath.Join(srcDir, "*.mochi"))
	total := len(files)
	compiled := 0
	var lines []string
	for _, f := range files {
		name := strings.TrimSuffix(filepath.Base(f), ".mochi")
		mark := "[ ]"
		if _, err := os.Stat(filepath.Join(outDir, name+".out")); err == nil {
			if _, err2 := os.Stat(filepath.Join(outDir, name+".error")); os.IsNotExist(err2) {
				compiled++
				mark = "[x]"
			}
		}
		lines = append(lines, fmt.Sprintf("- %s %s.mochi", mark, name))
	}
	var buf bytes.Buffer
	fmt.Fprintf(&buf, "# Mochi to Java Compiler Outputs (%d/%d compiled and run)\n\n", compiled, total)
	buf.WriteString("This directory contains Java source code generated by the Mochi compiler from the programs in `tests/vm/valid`. Each program was executed with `javac` and `java`. Successful runs have a corresponding `.out` file; failures include a `.error` file.\n\n")
	buf.WriteString("## Program checklist\n")
	buf.WriteString(strings.Join(lines, "\n"))
	buf.WriteString("\n\n## Remaining tasks\n")
	buf.WriteString("- [ ] Improve readability of generated query loops\n")
	buf.WriteString("- [ ] Reduce reliance on helper functions for simple builtins\n")
	if compiled == total {
		buf.WriteString("\nAll programs executed successfully.\n")
	}
	os.WriteFile(filepath.Join(outDir, "README.md"), buf.Bytes(), 0644)
}
