}
function exchange_sort($numbers) {
  $n = count($numbers);
  $i = 0;
  while ($i < $n) {
  $j = _iadd($i, 1);
  while ($j < $n) {
  if ($numbers[$j] < $numbers[$i]) {
  $temp = $numbers[$i];
  $numbers[$i] = $numbers[$j];
  $numbers[$j] = $temp;
}
  $j = _iadd($j, 1);
};
  $i = _iadd($i, 1);
};
  return $numbers;
}
