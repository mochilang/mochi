function binary_insertion_sort($arr) {
  $i = 1;
  while ($i < count($arr)) {
  $value = $arr[$i];
  $low = 0;
  $high = _isub($i, 1);
  while ($low <= $high) {
  $mid = _intdiv((_iadd($low, $high)), 2);
  if ($value < $arr[$mid]) {
  $high = _isub($mid, 1);
} else {
  $low = _iadd($mid, 1);
}
};
  $j = $i;
  while ($j > $low) {
  $arr[$j] = $arr[_isub($j, 1)];
  $j = _isub($j, 1);
};
