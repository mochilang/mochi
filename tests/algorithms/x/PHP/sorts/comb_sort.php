  function comb_sort(&$data) {
  $shrink_factor = 1.3;
  $gap = count($data);
  $completed = false;
  while (!$completed) {
  $gap = intval(_idiv($gap, $shrink_factor));
  if ($gap <= 1) {
  $gap = 1;
  $completed = true;
}
  $index = 0;
  while (_iadd($index, $gap) < count($data)) {
  if ($data[$index] > $data[_iadd($index, $gap)]) {
  $tmp = $data[$index];
  $data[$index] = $data[_iadd($index, $gap)];
  $data[_iadd($index, $gap)] = $tmp;
  $completed = false;
}
  $index = _iadd($index, 1);
};
};
  return $data;
};
