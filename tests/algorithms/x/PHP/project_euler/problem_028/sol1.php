function solution($n) {
  $total = 1;
  $i = 1;
  $limit = _intdiv((_iadd($n, 1)), 2);
  while ($i < $limit) {
  $odd = _iadd(_imul(2, $i), 1);
  $even = _imul(2, $i);
  $total = _isub(_iadd($total, _imul(_imul(4, $odd), $odd)), _imul(6, $even));
  $i = _iadd($i, 1);
};
  return $total;
}
