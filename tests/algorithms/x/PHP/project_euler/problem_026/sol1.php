  $the_digit = 1;
  $longest_len = 0;
  for ($d = $numerator; $d < $limit; $d++) {
  $remainders = [];
  $rem = $numerator;
  $count = 1;
  while ($count <= $limit) {
  if (contains($remainders, $rem)) {
  if ($longest_len < count($remainders)) {
  $longest_len = count($remainders);
  $the_digit = $d;
};
} else {
  $remainders = _append($remainders, $rem);
  $rem = _imod(_imul($rem, 10), $d);
}
  $count = _iadd($count, 1);
};
};
  return $the_digit;
}
