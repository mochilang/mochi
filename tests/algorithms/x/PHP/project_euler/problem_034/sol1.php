$DIGIT_FACTORIALS = [1, 1, 2, 6, 24, 120, 720, 5040, 40320, 362880];
function sum_of_digit_factorial($n) {
  global $DIGIT_FACTORIALS;
  if ($n == 0) {
  return $DIGIT_FACTORIALS[0];
}
  $total = 0;
  $num = $n;
  while ($num > 0) {
  $digit = _imod($num, 10);
  $total = _iadd($total, $DIGIT_FACTORIALS[$digit]);
  $num = _intdiv($num, 10);
};
  return $total;
}
function solution() {
  global $DIGIT_FACTORIALS;
  $limit = _iadd(_imul(7, $DIGIT_FACTORIALS[9]), 1);
  $total = 0;
  $i = 3;
  while ($i < $limit) {
  if (sum_of_digit_factorial($i) == $i) {
  $total = _iadd($total, $i);
}
  $i = _iadd($i, 1);
};
  return $total;
}
