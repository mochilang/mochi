}
function next_number($number) {
  $n = $number;
  $total = 0;
  while ($n > 0) {
  $d = _imod($n, 10);
  $total = _iadd($total, _imul($d, $d));
  $n = _intdiv($n, 10);
};
  return $total;
}
function chain($number) {
  $n = $number;
  while ($n != 1 && $n != 89) {
  $n = next_number($n);
};
  return $n == 1;
}
function solution($limit) {
  $count = 0;
  $i = 1;
  while ($i < $limit) {
  if (!chain($i)) {
  $count = _iadd($count, 1);
}
  $i = _iadd($i, 1);
};
  return $count;
}
