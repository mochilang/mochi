(def nowSeed (atom (let [s (System/getenv "MOCHI_NOW_SEED")] (if (and s (not (= s ""))) (Integer/parseInt s) 0))))

(declare potential_energy)

(def ^:dynamic main_G 9.80665)

(defn potential_energy [potential_energy_mass potential_energy_height]
  (try (do (when (< potential_energy_mass 0.0) (throw (Exception. "The mass of a body cannot be negative"))) (when (< potential_energy_height 0.0) (throw (Exception. "The height above the ground cannot be negative"))) (throw (ex-info "return" {:v (* (* potential_energy_mass main_G) potential_energy_height)}))) (catch clojure.lang.ExceptionInfo e (if (= (ex-message e) "return") (get (ex-data e) :v) (throw e)))))

(defn -main []
  (let [rt (Runtime/getRuntime)
    start-mem (- (.totalMemory rt) (.freeMemory rt))
    start (System/nanoTime)]
      (println (potential_energy 10.0 10.0))
      (println (potential_energy 10.0 5.0))
      (println (potential_energy 2.0 8.0))
      (System/gc)
      (let [end (System/nanoTime)
        end-mem (- (.totalMemory rt) (.freeMemory rt))
        duration-us (quot (- end start) 1000)
        memory-bytes (Math/abs ^long (- end-mem start-mem))]
        (println (str "{\n  \"duration_us\": " duration-us ",\n  \"memory_bytes\": " memory-bytes ",\n  \"name\": \"main\"\n}"))
      )
    ))

(-main)
