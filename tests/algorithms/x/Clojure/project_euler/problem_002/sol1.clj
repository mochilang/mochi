(declare solution)

(def ^:dynamic next_v nil)

(def ^:dynamic solution_i nil)

(def ^:dynamic solution_j nil)

(def ^:dynamic solution_total nil)

(defn solution [solution_n]
  (binding [next_v nil solution_i nil solution_j nil solution_total nil] (try (do (set! solution_i 1) (set! solution_j 2) (set! solution_total 0) (while (<= solution_j solution_n) (do (when (= (mod solution_j 2) 0) (set! solution_total (+ solution_total solution_j))) (set! next_v (+ solution_i solution_j)) (set! solution_i solution_j) (set! solution_j next_v))) (throw (ex-info "return" {:v solution_total}))) (catch clojure.lang.ExceptionInfo e (if (= (ex-message e) "return") (get (ex-data e) :v) (throw e))))))

(def ^:dynamic main_result (solution 4000000))

(defn -main []
  (let [rt (Runtime/getRuntime)
    start-mem (- (.totalMemory rt) (.freeMemory rt))
    start (System/nanoTime)]
      (println (str "solution() = " (str main_result)))
      (System/gc)
      (let [end (System/nanoTime)
        end-mem (- (.totalMemory rt) (.freeMemory rt))
        duration-us (quot (- end start) 1000)
        memory-bytes (Math/abs ^long (- end-mem start-mem))]
        (println (str "{\n  \"duration_us\": " duration-us ",\n  \"memory_bytes\": " memory-bytes ",\n  \"name\": \"main\"\n}"))
      )
    ))

(-main)
