import java.util.*;

public class ArrayLength {
    static void main() {
        List<String> arr = new ArrayList<>(Arrays.asList("apple", "orange", "pear"));
        System.out.println("Length of " + String.valueOf(arr) + " is " + String.valueOf(arr.size()) + ".");
    }
    public static void main(String[] args) {
    main();
    }
}
