package rosetta

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"testing"

	javacode "mochi/compiler/x/java"
	"mochi/parser"
	"mochi/types"
)

func stripJavaHeader(b []byte) []byte {
	if i := bytes.IndexByte(b, '\n'); i != -1 && bytes.HasPrefix(b, []byte("// Generated by Mochi")) {
		return b[i+1:]
	}
	return b
}

// TestMochiJavaGolden compiles Mochi Rosetta programs to Java and verifies
// the generated source matches the golden files in tests/rosetta/out/Java.
func TestMochiJavaGolden(t *testing.T) {
	if err := javacode.EnsureJavac(); err != nil {
		t.Skipf("javac not installed: %v", err)
	}

	root := findRepoRoot(t)
	srcDir := filepath.Join(root, "tests/rosetta/x/Mochi")
	outDir := filepath.Join(root, "tests/rosetta/out/Java")

	if err := os.MkdirAll(outDir, 0o755); err != nil {
		t.Fatalf("mkout: %v", err)
	}

	outs, err := filepath.Glob(filepath.Join(srcDir, "*.out"))
	if err != nil {
		t.Fatalf("glob: %v", err)
	}
	if len(outs) == 0 {
		t.Fatal("no Mochi Rosetta tests found")
	}

	for _, out := range outs {
		name := strings.TrimSuffix(filepath.Base(out), ".out")
		srcPath := filepath.Join(srcDir, name+".mochi")
		javaPath := filepath.Join(outDir, name+".java")
		if _, err := os.Stat(srcPath); err != nil {
			t.Fatalf("missing source for %s", name)
		}

		t.Run(name, func(t *testing.T) {
			prog, err := parser.Parse(srcPath)
			if err != nil {
				writeJavaError(outDir, name, fmt.Errorf("parse error: %w", err))
				t.Skip("parse error")
				return
			}
			env := types.NewEnv(nil)
			if errs := types.Check(prog, env); len(errs) > 0 {
				writeJavaError(outDir, name, fmt.Errorf("type error: %v", errs[0]))
				t.Skip("type error")
				return
			}
			code, err := javacode.New().Compile(prog)
			if err != nil {
				writeJavaError(outDir, name, fmt.Errorf("compile error: %w", err))
				t.Skip("compile error")
				return
			}
			got := stripJavaHeader(bytes.TrimSpace(code))

			if shouldUpdate() {
				if err := os.WriteFile(javaPath, append(got, '\n'), 0o644); err != nil {
					t.Fatalf("write java: %v", err)
				}
				t.Logf("updated: %s", javaPath)
				return
			}

			wantData, err := os.ReadFile(javaPath)
			if err != nil {
				t.Fatalf("read java golden: %v", err)
			}
			want := stripJavaHeader(bytes.TrimSpace(wantData))
			if !bytes.Equal(got, want) {
				t.Errorf("%s Java\n\n--- Got ---\n%s\n\n--- Want ---\n%s\n", name, got, want)
			}
			_ = os.Remove(filepath.Join(outDir, name+".error"))
		})
	}
}
