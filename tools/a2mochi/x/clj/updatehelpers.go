//go:build slow

package clj

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"time"
)

// UpdateReadmeForTests regenerates README checklist from golden outputs.
func UpdateReadmeForTests() {
	root := repoRoot()
	srcDir := filepath.Join(root, "tests", "transpiler", "x", "clj")
	outDir := filepath.Join(root, "tests", "a2mochi", "x", "clj")
	vmDir := filepath.Join(root, "tests", "vm", "valid")
	readme := filepath.Join(root, "tools", "a2mochi", "x", "clj", "README.md")

	files, _ := filepath.Glob(filepath.Join(srcDir, "*.clj"))
	sort.Strings(files)
	total := len(files)
	converted := 0
	var lines []string
	for _, f := range files {
		name := strings.TrimSuffix(filepath.Base(f), ".clj")
		mark := "[ ]"
		outPath := filepath.Join(outDir, name+".out")
		vmOut := filepath.Join(vmDir, name+".out")
		if outData, err := os.ReadFile(outPath); err == nil {
			if vmData, err2 := os.ReadFile(vmOut); err2 == nil {
				if bytes.Equal(bytes.TrimSpace(outData), bytes.TrimSpace(vmData)) {
					converted++
					mark = "[x]"
				}
			}
		}
		lines = append(lines, fmt.Sprintf("- %s %s", mark, name))
	}
	loc, _ := time.LoadLocation("Asia/Bangkok")
	ts := time.Now().In(loc).Format("2006-01-02 15:04 MST")
	var buf bytes.Buffer
	buf.WriteString("# a2mochi Clojure Converter\n\n")
	buf.WriteString("This directory holds golden outputs for the Clojure to Mochi converter. Each `.clj` source in `tests/transpiler/x/clj` has a matching `.mochi` and `.ast` file generated by the tests.\n\n")
	fmt.Fprintf(&buf, "Completed programs: %d/%d\n", converted, total)
	fmt.Fprintf(&buf, "Date: %s\n\n", ts)
	buf.WriteString("## Checklist\n")
	buf.WriteString(strings.Join(lines, "\n"))
	buf.WriteString("\n")
	_ = os.WriteFile(readme, buf.Bytes(), 0o644)
}

func repoRoot() string {
	dir, _ := os.Getwd()
	for i := 0; i < 10; i++ {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}
	return dir
}
