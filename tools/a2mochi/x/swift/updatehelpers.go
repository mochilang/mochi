//go:build slow

package swift

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"time"
)

// UpdateReadmeForTests regenerates README checklist from golden outputs.
func UpdateReadmeForTests() {
	root := repoRoot()
	srcDir := filepath.Join(root, "tests", "transpiler", "x", "swift")
	outDir := filepath.Join(root, "tests", "a2mochi", "x", "swift")
	readme := filepath.Join(root, "tools", "a2mochi", "x", "swift", "README.md")
	files, _ := filepath.Glob(filepath.Join(srcDir, "*.swift"))
	sort.Strings(files)
	total := len(files)
	compiled := 0
	var lines []string
	for _, f := range files {
		name := strings.TrimSuffix(filepath.Base(f), ".swift")
		mark := "[ ]"
		if _, err := os.Stat(filepath.Join(outDir, name+".mochi")); err == nil {
			compiled++
			mark = "[x]"
		}
		lines = append(lines, fmt.Sprintf("- %s %s", mark, name))
	}
	ts := time.Now().In(time.FixedZone("GMT+7", 7*3600)).Format("2006-01-02 15:04 MST")
	var buf bytes.Buffer
	buf.WriteString("# a2mochi Swift Converter\n\n")
	fmt.Fprintf(&buf, "Completed programs: %d/%d\n", compiled, total)
	fmt.Fprintf(&buf, "Date: %s\n\n", ts)
	buf.WriteString("This directory holds golden outputs for the Swift to Mochi converter. Each `.swift` source in `tests/transpiler/x/swift` has a matching `.mochi` and `.ast` file generated by the tests.\n\n")
	buf.WriteString("## Checklist\n")
	buf.WriteString(strings.Join(lines, "\n"))
	buf.WriteString("\n")
	_ = os.WriteFile(readme, buf.Bytes(), 0o644)
}
