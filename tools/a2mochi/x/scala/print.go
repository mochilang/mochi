package scala

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
	"time"

	"mochi/ast"
)

// Print returns Mochi source code for the given AST node, prefixed with a
// generated header containing the current version and timestamp.
func Print(node *ast.Node) (string, error) {
	var code strings.Builder
	if err := ast.Fprint(&code, node); err != nil {
		return "", err
	}
	var out strings.Builder
	out.WriteString(header())
	out.WriteString(code.String())
	return out.String(), nil
}

func header() string {
	loc := time.FixedZone("GMT+7", 7*3600)
	now := time.Now().In(loc)
	var b strings.Builder
	fmt.Fprintf(&b, "// Generated by a2mochi scala v%s on %s\n", version(), now.Format("2006-01-02 15:04:05 MST"))
	return b.String()
}

func version() string {
	root := repoRoot()
	if root == "" {
		return "dev"
	}
	data, err := os.ReadFile(filepath.Join(root, "VERSION"))
	if err != nil {
		return "dev"
	}
	return strings.TrimSpace(string(data))
}

func repoRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		return ""
	}
	for i := 0; i < 10; i++ {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}
	return ""
}
