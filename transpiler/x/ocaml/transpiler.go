//go:build slow

package ocaml

import (
	"bytes"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"strings"
	"time"

	"mochi/ast"
	"mochi/parser"
	"mochi/types"
)

// Program represents a sequence of OCaml statements.
type Program struct {
	Stmts []Stmt
}

// Stmt can emit itself as OCaml code.
type Stmt interface{ emit(io.Writer) }

// PrintStmt represents a call to print_endline with a string literal.
type PrintStmt struct{ Value string }

func (p *PrintStmt) emit(w io.Writer) {
	fmt.Fprintf(w, "  print_endline (%q);\n", p.Value)
}

func repoRoot() string {
	dir, err := os.Getwd()
	if err != nil {
		return ""
	}
	for i := 0; i < 10; i++ {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			break
		}
		dir = parent
	}
	return ""
}

func version() string {
	root := repoRoot()
	if root == "" {
		return "dev"
	}
	b, err := os.ReadFile(filepath.Join(root, "VERSION"))
	if err != nil {
		return "dev"
	}
	return strings.TrimSpace(string(b))
}

func header() string {
	loc := time.FixedZone("GMT+7", 7*3600)
	t := time.Now().In(loc)
	return fmt.Sprintf("(* Generated by Mochi transpiler v%s on %s *)\n", version(), t.Format("2006-01-02 15:04:05 MST"))
}

// Emit renders OCaml source code for the program.
func (p *Program) Emit() []byte {
	var buf bytes.Buffer
	buf.WriteString(header())
	buf.WriteString("\n")
	buf.WriteString("let () =\n")
	for _, s := range p.Stmts {
		s.emit(&buf)
	}
	return buf.Bytes()
}

// Transpile converts a Mochi program into a simple OCaml AST.
func Transpile(prog *parser.Program, env *types.Env) (*Program, error) {
	_ = env
	pr := &Program{}
	for _, st := range prog.Statements {
		if st.Expr != nil {
			call := st.Expr.Expr.Binary.Left.Value.Target.Call
			if call != nil && call.Func == "print" && len(call.Args) == 1 {
				arg := call.Args[0]
				lit := arg.Binary.Left.Value.Target.Lit
				if lit != nil && lit.Str != nil {
					pr.Stmts = append(pr.Stmts, &PrintStmt{Value: *lit.Str})
					continue
				}
			}
			return nil, fmt.Errorf("unsupported expression")
		}
		return nil, fmt.Errorf("unsupported statement")
	}
	return pr, nil
}

// Print converts the custom AST into a generic ast.Node and prints it.
func Print(p *Program) {
	n := &ast.Node{Kind: "program"}
	for _, st := range p.Stmts {
		switch s := st.(type) {
		case *PrintStmt:
			n.Children = append(n.Children, &ast.Node{Kind: "print", Value: s.Value})
		default:
			n.Children = append(n.Children, &ast.Node{Kind: "stmt"})
		}
	}
	n.Print("")
}
